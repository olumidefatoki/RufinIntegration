package Utils;

/**
 *
 * @author olumidefatoki
 */
public class RufinCustProfile {
    private String CBAcode;
    private int MFB;
    private String accountNumber;

    /**
     * @return the CBAcode
     */
    public String getCBAcode() {
        return CBAcode;
    }

    /**
     * @param CBAcode the CBAcode to set
     */
    public void setCBAcode(String CBAcode) {
        this.CBAcode = CBAcode;
    }

    /**
     * @return the MFB
     */
    public int getMFB() {
        return MFB;
    }

    /**
     * @param MFB the MFB to set
     */
    public void setMFB(int MFB) {
        this.MFB = MFB;
    }

    /**
     * @return the accountNumber
     */
    public String getAccountNumber() {
        return accountNumber;
    }

    /**
     * @param accountNumber the accountNumber to set
     */
    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }
}
